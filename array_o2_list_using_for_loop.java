public class array_o2_list_using_for_loop {
    public static void main(String[] args) {
        int[] marks = new int[3];
        marks[0] = 96;
        marks[1] = 99;
        marks[2] = 45;

        //printing marks using for loop
        for (int i = 0; i < 3; i++) {
            System.out.println(marks[i]);
        }
    }
}