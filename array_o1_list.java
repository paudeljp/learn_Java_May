import java.util.*;

public class array_o1_list {

    // Declare and initialize an array to store marks of three subjects
    public static void main(String[] args) {
        int[] marks = new int[3];
        marks[0] = 95;
        marks[1] = 90;
        marks[2] = 85;
        // Printing the marks of three subjects
        System.out.println(marks[0]);
        System.out.println(marks[1]);
        System.out.println(marks[2]);
    }
}
